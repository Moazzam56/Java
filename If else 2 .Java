public class EvenOddZero {
    public static void main(String[] args) {
        int num = 0;

        if (num == 0) {
            System.out.println("Zero");
        } else if (num % 2 == 0) {
            System.out.println("Even");
        } else {
            System.out.println("Odd");
        }
    }
}
